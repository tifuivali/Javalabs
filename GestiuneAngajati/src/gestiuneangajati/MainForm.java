    public void refreshSefiGrupeTable()
    {
         DefaultTableModel model=(DefaultTableModel) jTable_angajati.getModel();
            
            for(int i=model.getRowCount()-1;i>-1;i--)
            {
                model.removeRow(i);
            }
            for(Manager manager:gestiune.getManageri())
            {
        
                for(SefGrupa sefGrupa:manager.getSefiGrupe())
                 {
                   model.addRow(sefGrupa.getDetaliiAngajat());
                 }
            }
        
            jTable_angajati.setModel(model);
            jTable_angajati.doLayout();
          
    }
    
