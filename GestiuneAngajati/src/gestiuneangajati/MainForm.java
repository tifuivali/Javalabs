    private void Jtabbed_panelStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_Jtabbed_panelStateChanged
        // TODO add your handling code here:
        JTabbedPane sourceTabbedPane = (JTabbedPane) evt.getSource();
        int index=sourceTabbedPane.getSelectedIndex();
        if(index==3)
        {
           if(gestiune==null)
               return;
           jComboBox_manageri.removeAllItems();
            for(Manager manager:gestiune.getManageri())
            {
              jComboBox_manageri.addItem(manager.getNume()+" "+manager.getPrenume());
            }
            
        }
        if(index==2)
        {   jComboBox_manageri_AddsefEchipa.removeAllItems();
            for(Manager man:gestiune.getManageri())
            {
                jComboBox_manageri_AddsefEchipa.addItem(man.getNumePrenume());
                
            }
        }
        if(index==4)
        {
             jComboBox_addProgramator_manageri.removeAllItems();
            for(Manager man:gestiune.getManageri())
            {
                jComboBox_addProgramator_manageri.addItem(man.getNumePrenume());
                
            }
        }
        if(index==5)
        {
             jComboBox_addTester_manageri.removeAllItems();
            for(Manager man:gestiune.getManageri())
            {
                jComboBox_addTester_manageri.addItem(man.getNumePrenume());
                
            }
        }
    }//GEN-LAST:event_Jtabbed_panelStateChanged

