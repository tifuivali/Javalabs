    public void createTree()
    {
        DefaultMutableTreeNode root=new DefaultMutableTreeNode("Gestiune Angajati");
        DefaultTreeModel treeModel=new DefaultTreeModel(root);
        DefaultMutableTreeNode treeNode,treeNode1,treeNode2,treeNode3,treeNode4,treeNode5,treeNode6,treeNode7,treeNode8,treeNode9,treeNode10;       
        treeNode=new DefaultMutableTreeNode("Manageri");
        root.add(treeNode);
        for(Manager manager :gestiune.getManageri())
        {   
            treeNode2=new DefaultMutableTreeNode(manager.getNume()+" "+manager.getPrenume());
            treeNode3=new DefaultMutableTreeNode("Sefi grupe:");
            treeNode.add(treeNode2);
            treeNode2.add(treeNode3);
            for(SefGrupa sefGrupa:manager.getSefiGrupe())
            {   
                
                treeNode4=new DefaultMutableTreeNode(sefGrupa.getNume()+" "+sefGrupa.getPrenume());
                treeNode3.add(treeNode4);
                 treeNode5=new DefaultMutableTreeNode("Sef Echipe:");
                 treeNode4.add(treeNode5);
                
                for(SefEchipa sefEchipa:sefGrupa.getSefiEchipe())
                {
                    treeNode6=new DefaultMutableTreeNode(sefEchipa.getNume()+" "+sefEchipa.getPrenume());
                    treeNode5.add(treeNode6);
                     treeNode7=new DefaultMutableTreeNode("Programatori:");
                     treeNode8=new DefaultMutableTreeNode("Testeri");
                     treeNode6.add(treeNode7);
                     treeNode6.add(treeNode8);
                    
                    for(Programator programator:sefEchipa.getProgramatoriSubordonati())
                    {
                        treeNode9=new DefaultMutableTreeNode(programator.getNume()+" "+programator.getPrenume());
                        treeNode7.add(treeNode9);
                    }
                    
                    
                    for(Tester tester:sefEchipa.getTesteriSubordonati())
                    {
                        treeNode10=new DefaultMutableTreeNode(tester.getNume()+" "+tester.getPrenume());
                        treeNode8.add(treeNode10);
                    }
                    
                }
            }
        }
        
        
        jTree_Angajati.setModel(treeModel);
    }
    
    public void refreshManageriTable()
    {
         DefaultTableModel model=(DefaultTableModel) jTable_angajati.getModel();
            
            for(int i=model.getRowCount()-1;i>-1;i--)
            {
                model.removeRow(i);
            }
            for(Manager manager:gestiune.getManageri())
              { 
                 
                model.addRow(manager.getDetaliiAngajat());
                
                
              }
            jTable_angajati.setModel(model);
            jTable_angajati.doLayout();
    }
    
   


